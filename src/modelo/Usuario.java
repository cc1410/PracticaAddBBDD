package modelo;

/**
 *
 * @author chen
 */
public class Usuario {
    private String mail;
    private String password;
    private String name;
    private int type;

    public Usuario(){
        
    }
    public Usuario(String mail, String password, String name, int type) {
        this.mail = mail;
        this.password = password;
        this.name = name;
        this.type = type;
    }

    public String getMail() {
        return mail;
    }

    public void setMail(String mail) {
        this.mail = mail;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }
    
    
    
}
